import numpy as np

def add(a,b):
    """ Add a to b and return the result. """
    return a + b

def subtract(a, b):
    """ Subtract b from a and return the result. """
    return a - b

def multiply(a, b):
    """ Multiply a and b and return the result. """
    return a * b

def divide(a, b):
    """ Divide a by b and return the result. """
    return a / b

def product(arr):
    """ Product of all number in an array. """
    return np.prod(arr)
